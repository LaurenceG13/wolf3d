#include "includes/libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t			i;
	unsigned char	letter;
	unsigned char	*d;
	unsigned char	*s;

	letter = (unsigned char)c;
	d = (unsigned char *)dst;
	s = (unsigned char *)src;
	i = 0;
	while (i < n)
	{
		d[i] = s[i];
		if (d[i] == letter)
			return (&dst[i + 1]);
		i++;
	}
	return (NULL);
}
